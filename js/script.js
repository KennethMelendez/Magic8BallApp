function magic8ball(){
  var magic8ballResponses = [];
    //can create new responses and add arrays below
    magic8ballResponses[0] = 'Yes..!';
    magic8ballResponses[1] = 'nope.';
    magic8ballResponses[2] = 'maybe....not too sure';
    magic8ballResponses[3] = 'Yes.....DEFINITELY!';
    magic8ballResponses[4] = 'Hahaha...  no';
    magic8ballResponses[5] = 'Sorry..but no..';
    magic8ballResponses[6] = 'Without a doubt.';

      //generates a random number based on array length
      var randomResponse = Math.floor(Math.random() * magic8ballResponses.length);
      //generates random response based on the random number generated from random response.Outputs a response
      var randomResponseOutput = magic8ballResponses[randomResponse];
      //Returns a response generated by the program
      return randomResponseOutput;
}

