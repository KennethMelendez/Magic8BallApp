//variables for locating the dom
var userResponse = document.querySelector('#userResponse');     //MAGIC8BALL WILL RESPONSE!
var userInput = document.querySelector('#userInput');           //The text box
var userBtn = document.querySelector('#userBtn');               //The button!

//click event to generate a response
userBtn.addEventListener('click',function(){

  var userInputValue = userInput.value;

  if (userInputValue === ''){
    userResponse.textContent = '* Please write a response.';
  } else if (userInputValue == Number(userInputValue)){
    userResponse.textContent = '* Please write a question';
  } else {
    userInput.value = '';
    userResponse.textContent = magic8ball();
  }
});
